oledssd1306.initDisplay()
BME280.PowerOn()
BME280.Address(BME280_I2C_ADDRESS.ADDR_0x76)
basic.forever(function () {
    oledssd1306.setTextXY(0, 0)
    oledssd1306.writeString("Temperatur:")
    oledssd1306.setTextXY(1, 0)
    oledssd1306.writeNumber(BME280.temperature(BME280_T.T_C))
    oledssd1306.writeString(" Â°C")
    oledssd1306.setTextXY(3, 0)
    oledssd1306.writeString("Luftdruck:")
    oledssd1306.setTextXY(4, 0)
    oledssd1306.writeNumber(BME280.pressure(BME280_P.hPa))
    oledssd1306.writeString(" hPa")
    basic.pause(1000)
})
